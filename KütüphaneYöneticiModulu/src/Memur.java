public class Memur {
    
    private int memurID;
    private String memurAd;
    private String memurSoyad;
    private String memurEmail;
    private String memurParola;

    public Memur(int memurID, String memurAd, String memurSoyad, String memurEmail, String memurParola) {
        this.memurID = memurID;
        this.memurAd = memurAd;
        this.memurSoyad = memurSoyad;
        this.memurEmail = memurEmail;
        this.memurParola = memurParola;
    }

    public int getMemurID() {
        return memurID;
    }

    public void setMemurID(int memurID) {
        this.memurID = memurID;
    }

    public String getMemurAd() {
        return memurAd;
    }

    public void setMemurAd(String memurAd) {
        this.memurAd = memurAd;
    }

    public String getMemurSoyad() {
        return memurSoyad;
    }

    public void setMemurSoyad(String memurSoyad) {
        this.memurSoyad = memurSoyad;
    }

    public String getMemurEmail() {
        return memurEmail;
    }

    public void setMemurEmail(String memurEmail) {
        this.memurEmail = memurEmail;
    }

    public String getMemurParola() {
        return memurParola;
    }

    public void setMemurParola(String memurParola) {
        this.memurParola = memurParola;
    }

    
    
}

