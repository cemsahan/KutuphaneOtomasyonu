    public void memurGoruntule(){
        model1.setRowCount(0);
        ArrayList<Memur> gorevliler=new ArrayList<Memur>();
        
        gorevliler=memurIslemleri.memurlariGetir();
        if(gorevliler!=null){
            for(Memur gorevli:gorevliler){
                
                Object[] eklenecek={gorevli.getMemurID(),gorevli.getMemurAd(),gorevli.getMemurSoyad(),gorevli.getMemurEmail(),gorevli.getMemurParola()};
                model1.addRow(eklenecek);
                
                
                
                
                
            }
            
        }
        
        
        
    }
    public void UyeGoruntule(){
        
        model.setRowCount(0);
        
        ArrayList<Uyeler> uyeler=new ArrayList<Uyeler>();
        
        uyeler=uyeIslemleri.uyeleriGetir();
        if(uyeler!=null)
        {
            for(Uyeler uye:uyeler){
                Object[] eklenecek={uye.getUyeAdi(),uye.getUyeSoyad(),uye.getParola(),uye.getTel(),uye.getEmail(),uye.getCinsiyet()
                ,uye.getSehir(),uye.getIlce(),uye.getMahalle(),uye.getSokak(),uye.getBinaNo(),uye.getDaireNo(),uye.getPostaKodu(),uye.getUyeNo()};
                model.addRow(eklenecek);
                
            }
        }
       
    }
