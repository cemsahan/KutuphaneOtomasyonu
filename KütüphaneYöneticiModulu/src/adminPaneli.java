    private void btn_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guncelleActionPerformed

        int selectedRow=tbl_uyeler.getSelectedRow();

        if(selectedRow==-1){

            if(model.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(this, "Üyeler tablosu şu anda boş...");
            }
            else {
                JOptionPane.showMessageDialog(null, "Lütfen güncellenecek üyeyi seçiniz...");
            }
        }
        else {
            String ad=tf_uyeAdi.getText();
            String soyad=tf_uyeSoyadi.getText();
            String parola=tf_uyeParola.getText();
            String email=tf_uyeEmail.getText();
            String telefon=tf_uyeTel.getText();
            String cinsiyet;
            if(rb_erkek.isSelected()) cinsiyet="erkek";
            else cinsiyet="kadın";
            String il=tf_il.getText();
            String ilce=tf_ilce.getText();
            String mahalle=tf_mahalle.getText();
            String sokak=tf_sokak.getText();
            String binaNo=tf_binaNo.getText();
            String daireNo=tf_daireNo.getText();
            String postaKodu=tf_postaKodu.getText();
            int uyeNo=(int)model.getValueAt(selectedRow, 13);
            uyeIslemleri.uyeGuncelle(ad, soyad, parola, telefon,email, cinsiyet, uyeNo);
            uyeIslemleri.adresGuncelle(il, ilce, mahalle, sokak, binaNo, daireNo, postaKodu, uyeNo);
            UyeGoruntule();

        }

    }//GEN-LAST:event_btn_guncelleActionPerformed

