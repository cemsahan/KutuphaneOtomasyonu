    private void btn_gorevliGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_gorevliGuncelleActionPerformed
        int selectedRow=tbl_memur.getSelectedRow();

        if(selectedRow==-1){

            if(model.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(this, "Memurlar tablosu şu anda boş...");
            }
            else {
                JOptionPane.showMessageDialog(null, "Lütfen güncellenecek memuru seçiniz...");
            }
        }
        else {
            String memurAd=tf_memurAdi.getText();
            String memurSoyad=tf_memurSoyad.getText();
            String memurEmail=tf_memurEmail.getText();
            String memurParola=tf_memurParola.getText();
            int memurID=(int)model1.getValueAt(selectedRow, 0);
            memurIslemleri.memurGuncelle(memurAd, memurSoyad, memurEmail, memurParola, memurID);
           memurGoruntule();
            JOptionPane.showMessageDialog(null, "Görevli başarıyla güncellendi...");

        }

    }//GEN-LAST:event_btn_gorevliGuncelleActionPerformed

